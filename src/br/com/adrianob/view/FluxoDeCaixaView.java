    @Override
    public void actionPerformed(ActionEvent e) {
        if (e.getActionCommand().equalsIgnoreCase("selecionar")) {
            if (lcv != null) {
                fluxoDeCaixa.setConta(lcv.getContaSelecionada());
                lcv.dispose();
                fluxoDeCaixa.setTipo(fluxoDeCaixa.getConta().getTipo());
            }
        }
        if (e.getActionCommand().equalsIgnoreCase("selecionarCC")) {
            if (ccv != null) {
                fluxoDeCaixa.setContaCorrente(ccv.getContaCorrenteSelecionada());
                ccv.dispose();
            }
        }
    }
