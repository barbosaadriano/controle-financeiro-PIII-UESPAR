    private void btnProcessarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnProcessarActionPerformed
        if (JOptionPane.showConfirmDialog(this, "Deseja realizar este lançamento?",
                "Confirmação", JOptionPane.YES_NO_OPTION, JOptionPane.QUESTION_MESSAGE)
                == JOptionPane.NO_OPTION) {
            return;
        }
        if (!validar()) {
            return;
        }
        float saldo = fluxoDeCaixa.getContaCorrente().getSaldo();
        if (fluxoDeCaixa.getTipo().equalsIgnoreCase("entrada")) {
            saldo += fluxoDeCaixa.getValor();
        } else {
            saldo -= fluxoDeCaixa.getValor();
        }
        fluxoDeCaixa.getContaCorrente().setSaldo(saldo);
        fluxoDeCaixa.getContaCorrente().setUltimaAtualizacao(fluxoDeCaixa.getDataDeMovimento());
        List<Object> lst = new ArrayList<Object>();
        lst.add(fluxoDeCaixa);
        DaoGenerico.getInstance().salvarOuRemover(lst, DaoGenerico.SALVAR);
        DaoGenerico.getInstance().closeEm();
        JOptionPane.showMessageDialog(this, "Lançamento registrado!");
        if (JOptionPane.showConfirmDialog(this, "Deseja fazer outros lançamentos?",
                "Confirmação", JOptionPane.YES_NO_OPTION, JOptionPane.QUESTION_MESSAGE)
                == JOptionPane.YES_OPTION) {
            this.setFluxoDeCaixa(new FluxoDeCaixa());
        } else {
            dispose();
        }
    }//GEN-LAST:event_btnProcessarActionPerformed

