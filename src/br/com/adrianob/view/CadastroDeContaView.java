    public void setContaSelecionada(Conta contaSelecionada) {
        Conta oldContaSelecionada = this.contaSelecionada;
        this.contaSelecionada = contaSelecionada;
        if (contaSelecionada != null && contaSelecionada.getContaPai() == null) {
            txtContaPai.setText("");
            txtIdContaPai.setText("");
        }
        propertyChangeSupport.firePropertyChange(PROP_CONTASELECIONADA, oldContaSelecionada, contaSelecionada);
    }

