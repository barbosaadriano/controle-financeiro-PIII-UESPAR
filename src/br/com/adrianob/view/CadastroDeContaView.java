    @Override
    public void actionPerformed(ActionEvent e) {
        if (e.getActionCommand().equalsIgnoreCase("selecionar")) {
            if (lv != null) {
                if (contaSelecionada.getId() == lv.getContaSelecionada().getId()) {
                    JOptionPane.showMessageDialog(this, "Você não pode vincular "
                            + "uma conta a ela mesma!", "Alerta", JOptionPane.WARNING_MESSAGE);
                    return;
                }
              
                contaSelecionada.setContaPai(lv.getContaSelecionada());
                lv.dispose();
            }
        }
    }
