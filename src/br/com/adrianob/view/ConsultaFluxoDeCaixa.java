    private void listar() {
        listaFluxoDeCaixa.clear();
        Map<String, Object> par = new HashMap<String, Object>();
        par.put("c", contaSelecionada);
        List listar = DaoGenerico.getInstance().listar("Select f from br.com.adrianob.model.FluxoDeCaixa f where f.contaCorrente = :c",
                par);
        listaFluxoDeCaixa.addAll(listar);
    }

