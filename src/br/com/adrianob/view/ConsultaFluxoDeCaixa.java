    public void actionPerformed(ActionEvent e) {
        if (e.getActionCommand().equalsIgnoreCase("selecionarcc")) {
            if (ccv != null) {
                this.setContaSelecionada(ccv.getContaCorrenteSelecionada());
                ccv.dispose();
                this.listar();
            }
        }
    }
